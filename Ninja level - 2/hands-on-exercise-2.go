package main

import "fmt"

func main() {
	a := (10 == 10)
	b := (10 <= 13)
	c := (10 >= 13)
	d := (10 != 13)
	e := (10 < 13)
	f := (10 > 13)

	fmt.Println(a, b, c, d, e, f)
}

